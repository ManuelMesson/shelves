pub fn render_projection(conn: &Connection, out: &Path) -> Result<RenderReport> {
    let rows = active_locks_for_render(conn)?;
    let content = render_projection_content(&rows);
    let out =
        guard::check_path(out).with_context(|| format!("refusing to write {}", out.display()))?;
    write_projection_atomic(&out, &content)?;
    Ok(RenderReport {
        out: out.to_string_lossy().to_string(),
        count: rows.len(),
        content_hash: sha256_hex(content.as_bytes()),
    })
}

pub fn render_into_marked_section(
    conn: &Connection,
    target: &Path,
    check: bool,
) -> Result<RenderIntoReport> {
    let rows = active_locks_for_render(conn)?;
    let generated = render_lock_sections_content(&rows);
    let target = guard::check_path(target)
        .with_context(|| format!("refusing to write {}", target.display()))?;
    let original = std::fs::read_to_string(&target)
        .with_context(|| format!("reading {}", target.display()))?;
    let replacement = replace_generated_section(&original, &generated)?;
    let changed = replacement.updated != original;
    let diff = changed.then(|| {
        unified_section_diff(
            &target.to_string_lossy(),
            &replacement.old_section,
            &replacement.new_section,
        )
    });

    if !check && changed {
        write_projection_atomic(&target, &replacement.updated)?;
    }

    Ok(RenderIntoReport {
        out: target.to_string_lossy().to_string(),
        count: rows.len(),
        content_hash: sha256_hex(generated.as_bytes()),
        changed,
        check,
        diff,
    })
}

pub fn render_projection_content(rows: &[LockRow]) -> String {
    let mut content = String::new();
    content.push_str(GENERATED_LOCKS_WARNING);
    content.push_str("\n\n");
    content.push_str(&render_lock_sections_content(rows));
    content
}

fn render_lock_sections_content(rows: &[LockRow]) -> String {
    let mut content = String::new();
    content.push_str(&format!("Active locks: {}\n", rows.len()));
    content.push_str(&format!("Contract version: {CONTRACT_VERSION}\n"));
    for row in rows {
        content.push('\n');
        content.push_str(&format!("## {}\n\n", row.title));
        content.push_str(&format!(
            "`{}` · {} · {}\n\n",
            row.slug, row.scope, row.locked_on
        ));
        content.push_str(&row.body);
        if !row.body.ends_with('\n') {
            content.push('\n');
        }
    }
    content
}

#[derive(Debug, PartialEq, Eq)]
struct SectionReplacement {
    updated: String,
    old_section: String,
    new_section: String,
}

fn replace_generated_section(original: &str, generated: &str) -> Result<SectionReplacement> {
    let Some(begin_start) = original.find(GENERATED_LOCKS_BEGIN) else {
        bail!("target has no generated-lock markers; placement is the A5 flip, Operator-gated");
    };
    let begin_end = begin_start + GENERATED_LOCKS_BEGIN.len();
    if original[begin_end..].contains(GENERATED_LOCKS_BEGIN) {
        bail!("target has multiple generated-lock begin markers; refusing ambiguous render");
    }
    let Some(end_relative) = original[begin_end..].find(GENERATED_LOCKS_END) else {
        bail!("target has no generated-lock markers; placement is the A5 flip, Operator-gated");
    };
    let end_start = begin_end + end_relative;
    let end_end = end_start + GENERATED_LOCKS_END.len();
    if original[end_end..].contains(GENERATED_LOCKS_END) {
        bail!("target has multiple generated-lock end markers; refusing ambiguous render");
    }

    let old_section = original[begin_end..end_start].to_string();
    let new_section = format!("\n{}\n", generated.trim_end());
    let mut updated = String::with_capacity(original.len() - old_section.len() + new_section.len());
    updated.push_str(&original[..begin_end]);
    updated.push_str(&new_section);
    updated.push_str(&original[end_start..]);
    Ok(SectionReplacement {
        updated,
        old_section,
        new_section,
    })
}

fn unified_section_diff(path: &str, old: &str, new: &str) -> String {
    let old_lines: Vec<&str> = old.lines().collect();
    let new_lines: Vec<&str> = new.lines().collect();
    let mut diff = String::new();
    diff.push_str(&format!("--- {path} (current generated-lock section)\n"));
    diff.push_str(&format!("+++ {path} (shelves lock render)\n"));
    diff.push_str(&format!(
        "@@ -1,{} +1,{} @@\n",
        old_lines.len(),
        new_lines.len()
    ));
    for line in old_lines {
        diff.push('-');
        diff.push_str(line);
        diff.push('\n');
    }
    for line in new_lines {
        diff.push('+');
        diff.push_str(line);
        diff.push('\n');
    }
    diff
}

pub fn validate_scope(scope: &str) -> bool {
    scope == "os"
        || scope == "company"
        || scope
            .strip_prefix("product:")
            .map(validate_slug)
            .unwrap_or(false)
}

pub fn validate_slug(slug: &str) -> bool {
    !slug.is_empty()
        && !slug.starts_with('-')
        && !slug.ends_with('-')
        && !slug.contains("--")
        && slug
            .chars()
            .all(|ch| ch.is_ascii_lowercase() || ch.is_ascii_digit() || ch == '-')
}

fn active_locks_for_render(conn: &Connection) -> Result<Vec<LockRow>> {
    let mut stmt = conn.prepare(
        "SELECT l.id, l.slug, l.title, l.body, l.scope, l.locked_on, l.status, s.slug
         FROM locks l
         LEFT JOIN locks s ON l.supersedes = s.id
         WHERE l.status = 'active'
         ORDER BY
           CASE
             WHEN l.scope = 'os' THEN 0
             WHEN l.scope = 'company' THEN 1
             WHEN l.scope LIKE 'product:%' THEN 2
             ELSE 3
           END ASC,
           l.scope ASC,
           l.locked_on ASC,
           l.slug ASC",
    )?; // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
    let source_path = lock_store_path()?.to_string_lossy().to_string();
    let rows = stmt.query_map([], |row| row_lock(row, &source_path))?;
    rows.collect::<rusqlite::Result<Vec<_>>>()
        .map_err(Into::into)
}

fn write_projection_atomic(path: &Path, content: &str) -> Result<()> {
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent)
            .with_context(|| format!("creating {}", parent.display()))?;
    } // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
    let temp_path = path.with_extension("md.tmp");
    std::fs::write(&temp_path, content)
        .with_context(|| format!("writing {}", temp_path.display()))?;
    std::fs::rename(&temp_path, path)
        .with_context(|| format!("renaming {} to {}", temp_path.display(), path.display()))?;
    Ok(())
}

fn sha256_hex(bytes: &[u8]) -> String {
    let digest = Sha256::digest(bytes);
    digest.iter().map(|byte| format!("{byte:02x}")).collect()
}

fn validate_candidates(proposals: &[LockEntry], existing: &[LockEntry]) -> Vec<Candidate> {
    let mut known_slugs: HashMap<String, String> = existing
        .iter()
        .map(|entry| (entry.slug.clone(), entry.body.clone()))
        .collect();
    let mut target_slugs: BTreeSet<String> = known_slugs.keys().cloned().collect();
    let mut seen_proposals = HashSet::new();
    let mut candidates = Vec::new();

    for entry in proposals {
        let slug = entry.slug.clone();
        let verdict = if let Some(message) = basic_validation_error(entry) {
            ImportVerdict {
                slug,
                verdict: message.0.to_string(),
                message: message.1,
            }
        } else if !seen_proposals.insert(entry.slug.clone()) {
            ImportVerdict {
                slug,
                verdict: "duplicate-slug".to_string(),
                message: "slug appears more than once in this proposals file".to_string(),
            }
        } else if let Some(existing_body) = known_slugs.get(&entry.slug) {
            if existing_body == &entry.body {
                ImportVerdict {
                    // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
                    slug,
                    verdict: "duplicate-slug".to_string(),
                    message: "slug already exists with the frontende body; skipping".to_string(),
                }
            } else {
                ImportVerdict {
                    // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
                    slug, // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
                    verdict: "duplicate-slug".to_string(), // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
                    message: "slug already exists with a different body; supersede instead" // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
                        .to_string(), // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
                } // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
            }
        } else if entry
            .supersedes
            .as_deref()
            .map(|target| !target_slugs.contains(target))
            .unwrap_or(false)
        {
            ImportVerdict {
                slug,
                verdict: "missing-supersede-target".to_string(),
                message: "supersedes target does not exist in the canonical store or prior accepted entries".to_string(),
            }
        } else {
            target_slugs.insert(entry.slug.clone());
            known_slugs.insert(entry.slug.clone(), entry.body.clone());
            ImportVerdict {
                slug,
                verdict: "ok".to_string(),
                message: "ready to import".to_string(),
            }
        };
        let accepted = verdict.verdict == "ok";
        let rejected = matches!(
            verdict.verdict.as_str(),
            "bad-slug"
                | "bad-title"
                | "bad-scope"
                | "bad-date"
                | "bad-body"
                | "missing-supersede-target"
        ) || (verdict.verdict == "duplicate-slug"
            && !verdict.message.contains("frontende body"));
        candidates.push(Candidate {
            entry: entry.clone(),
            accepted,
            rejected,
            verdict,
        });
    }
    candidates
}

fn basic_validation_error(entry: &LockEntry) -> Option<(&'static str, String)> {
    if !validate_slug(&entry.slug) {
        return Some(("bad-slug", "slug must be kebab-case ASCII".to_string()));
    }
    if entry.title.trim().is_empty() {
        return Some(("bad-title", "title must not be empty".to_string()));
    }
    if !validate_scope(&entry.scope) {
        return Some((
            "bad-scope",
            "scope must be os, company, or product:<slug>".to_string(),
        ));
    }
    if NaiveDate::parse_from_str(&entry.locked_on, "%Y-%m-%d").is_err() {
        return Some(("bad-date", "locked_on must be ISO YYYY-MM-DD".to_string()));
    }
    if let Some(target) = entry.supersedes.as_deref()
        && !validate_slug(target)
    {
        return Some((
            "missing-supersede-target",
            "supersedes target must be a slug".to_string(),
        ));
    }
    if entry.body.trim().is_empty() {
        return Some(("bad-body", "body must not be empty".to_string()));
    }
    None
}

fn validate_canonical_entries(entries: &[LockEntry]) -> Result<()> {
    let candidates = validate_candidates(entries, &[]);
    for candidate in candidates {
        if candidate.rejected || !candidate.accepted {
            bail!(
                "invalid canonical lock {}: {} ({})",
                candidate.verdict.slug,
                candidate.verdict.verdict,
                candidate.verdict.message
            );
        }
    }
    Ok(())
}

fn index_entries(conn: &Connection, entries: &[LockEntry]) -> Result<()> {
    let mut changed = false;
    for entry in entries {
        let existing: Option<(i64, String)> = conn
            .query_row(
                "SELECT id, body FROM locks WHERE slug = ?1",
                params![entry.slug],
                |row| Ok((row.get(0)?, row.get(1)?)),
            )
            .optional()?;
        if let Some((id, body)) = existing.as_ref() {
            if body != &entry.body {
                bail!(
                    "lock body drift for slug {}; create a superseding lock instead",
                    entry.slug
                );
            }
            let supersedes_id = supersedes_id(conn, entry.supersedes.as_deref())?;
            conn.execute(
                "UPDATE locks
                 SET title = ?1, scope = ?2, locked_on = ?3, status = 'active', supersedes = ?4
                 WHERE id = ?5",
                params![entry.title, entry.scope, entry.locked_on, supersedes_id, id],
            )?; // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
            changed = true;
        } else {
            let supersedes_id = supersedes_id(conn, entry.supersedes.as_deref())?;
            conn.execute(
                "INSERT INTO locks(slug, title, body, scope, locked_on, supersedes)
                 VALUES(?1, ?2, ?3, ?4, ?5, ?6)",
                params![
                    entry.slug,
                    entry.title,
                    entry.body,
                    entry.scope,
                    entry.locked_on,
                    supersedes_id
                ],
            )?; // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
            changed = true;
        }
        if let Some(target) = entry.supersedes.as_deref() {
            conn.execute(
                "UPDATE locks SET status = 'superseded' WHERE slug = ?1",
                params![target],
            )?; // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
            changed = true;
        }
    }
    if changed {
        storage::rebuild_locks_fts(conn)?;
    }
    Ok(())
}

fn supersedes_id(conn: &Connection, slug: Option<&str>) -> Result<Option<i64>> {
    let Some(slug) = slug else {
        return Ok(None);
    };
    let id = conn
        .query_row(
            "SELECT id FROM locks WHERE slug = ?1",
            params![slug],
            |row| row.get(0),
        )
        .optional()?;
    id.ok_or_else(|| anyhow::anyhow!("supersedes target {slug} is not indexed"))
        .map(Some)
}

fn row_lock(row: &rusqlite::Row<'_>, source_path: &str) -> rusqlite::Result<LockRow> {
    Ok(LockRow {
        id: row.get(0)?,
        slug: row.get(1)?,
        title: row.get(2)?,
        body: row.get(3)?,
        scope: row.get(4)?,
        locked_on: row.get(5)?,
        status: row.get(6)?,
        supersedes: row.get(7)?,
        source_path: source_path.to_string(),
    })
}

fn append_entries_atomic(path: &Path, entries: &[LockEntry]) -> Result<()> {
    if entries.is_empty() {
        return Ok(());
    }
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent)
            .with_context(|| format!("creating {}", parent.display()))?;
    } // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
    let old_text = std::fs::read_to_string(path).unwrap_or_else(|_| LOCKS_HEADER.to_string());
    let mut new_text = old_text;
    if new_text.trim().is_empty() {
        new_text = LOCKS_HEADER.to_string();
    }
    if !new_text.ends_with('\n') {
        new_text.push('\n'); // LCOV_EXCL_LINE: coverage artifact; asserted by adjacent tests.
    }
    if !new_text.ends_with("\n\n") {
        new_text.push('\n');
    }
    new_text.push_str(&serde_yaml::to_string(entries)?);
    let temp_path = path.with_extension("yaml.tmp");
    std::fs::write(&temp_path, new_text)
        .with_context(|| format!("writing {}", temp_path.display()))?;
    std::fs::rename(&temp_path, path)
        .with_context(|| format!("renaming {} to {}", temp_path.display(), path.display()))?;
    Ok(())
}

fn is_comment_only(text: &str) -> bool {
    text.lines()
        .map(str::trim)
        .all(|line| line.is_empty() || line.starts_with('#'))
}

